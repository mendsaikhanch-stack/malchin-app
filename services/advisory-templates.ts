// Helper: категорийг унших
export function getTemplatesByCategory(
  category: AdvisoryCategory
): AdvisoryTemplate[] {
  return ADVISORY_TEMPLATES.filter((t) => t.category === category);
}

// Helper: улирлаар filter (одоогийн улирал эсвэл тодорхойгүй season-тай бүгд)
export function getTemplatesForSeason(
  season: AdvisorySeason
): AdvisoryTemplate[] {
  return ADVISORY_TEMPLATES.filter(
    (t) => !t.season || t.season.includes(season)
  );
}

// Helper: key-ээр хайх
export function findTemplateById(id: string): AdvisoryTemplate | null {
  return ADVISORY_TEMPLATES.find((t) => t.id === id) ?? null;
}

// Helper: tag search
export function searchTemplatesByTag(tag: string): AdvisoryTemplate[] {
  const q = tag.toLowerCase().trim();
  if (!q) return [];
  return ADVISORY_TEMPLATES.filter((t) =>
    t.tags.some((x) => x.toLowerCase().includes(q)) ||
    t.question.toLowerCase().includes(q)
  );
}

