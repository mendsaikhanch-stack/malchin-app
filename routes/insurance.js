// Бүх даатгалын төрлүүд
router.get("/types", (req, res) => {
  res.json(INSURANCE_TYPES);
});

// Нэг даатгалын төрөл
router.get("/types/:type", (req, res) => {
  const item = INSURANCE_TYPES.find(t => t.type === req.params.type);
  if (!item) return res.status(404).json({ error: "Олдсонгүй" });
  res.json(item);
});

// Халамжийн хөтөлбөрүүд
router.get("/welfare", (req, res) => {
  const { category } = req.query;
  if (category) {
    const filtered = WELFARE_PROGRAMS.filter(w => w.category === category);
    return res.json(filtered);
  }
  res.json(WELFARE_PROGRAMS);
});

// Бүрдүүлэх бичиг баримт
router.get("/documents", (req, res) => {
  const { type } = req.query;
  if (type) {
    const filtered = DOCUMENTS.filter(d => d.type === type);
    return res.json(filtered);
  }
  res.json(DOCUMENTS);
});

// Холбоо барих
router.get("/contacts", (req, res) => {
  res.json(CONTACTS);
});

// Шимтгэлийн тооцоолуур
router.get("/calculate", (req, res) => {
  const income = parseInt(req.query.income) || 500000;
  res.json(calculatePremium(income));
});

// Бүгдийг нэг дор авах (дэлгэцэд ачаалахад)
router.get("/all", (req, res) => {
  res.json({
    insurance_types: INSURANCE_TYPES,
    welfare_programs: WELFARE_PROGRAMS,
    documents: DOCUMENTS,
    contacts: CONTACTS,
    default_calculation: calculatePremium(500000),
  });
});

