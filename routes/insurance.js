// Шимтгэлийн тооцоолуур
function calculatePremium(monthlyIncome) {
  const pension = monthlyIncome * 0.07;
  const benefit = monthlyIncome * 0.01;
  const accident = monthlyIncome * 0.01;
  const unemployment = monthlyIncome * 0.005;
  const health = monthlyIncome * 0.02;
  const total = pension + benefit + accident + unemployment + health;
  return {
    monthly_income: monthlyIncome,
    pension: Math.round(pension),
    benefit: Math.round(benefit),
    accident: Math.round(accident),
    unemployment: Math.round(unemployment),
    health: Math.round(health),
    total: Math.round(total),
    percent: "11.5%",
    yearly: Math.round(total * 12),
  };
}

